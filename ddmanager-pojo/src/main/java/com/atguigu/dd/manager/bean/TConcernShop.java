package com.atguigu.dd.manager.bean;

public class TConcernShop {
    private Integer shopId;

    private Integer userId;

    private Integer concernStatus;

    private Integer wanttoStatus;

    public Integer getShopId() {
        return shopId;
    }

    public void setShopId(Integer shopId) {
        this.shopId = shopId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getConcernStatus() {
        return concernStatus;
    }

    public void setConcernStatus(Integer concernStatus) {
        this.concernStatus = concernStatus;
    }

    public Integer getWanttoStatus() {
        return wanttoStatus;
    }

    public void setWanttoStatus(Integer wanttoStatus) {
        this.wanttoStatus = wanttoStatus;
    }
}